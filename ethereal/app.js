const page = document.body.dataset.page;
const storageKey = "oneworld-ethereal-lang";

function getValue(object, path) {
  return path.split(".").reduce((acc, part) => (acc ? acc[part] : undefined), object);
}

function translatePage(lang) {
  const dictionary = translations[lang];
  if (!dictionary || !dictionary[page]) {
    return;
  }

  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  document.body.dataset.lang = lang;

  const metaTitle = dictionary[page].metaTitle;
  const metaDescription = dictionary[page].metaDescription;

  if (metaTitle) {
    document.title = metaTitle;
  }

  if (metaDescription) {
    const meta = document.querySelector('meta[name="description"]');
    if (meta) {
      meta.setAttribute("content", metaDescription);
    }
  }

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = getValue(dictionary, node.dataset.i18n);
    if (typeof value === "string") {
      node.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((node) => {
    const [attribute, key] = node.dataset.i18nAttr.split(":");
    const value = getValue(dictionary, key);
    if (attribute && typeof value === "string") {
      node.setAttribute(attribute, value);
    }
  });

  document.querySelectorAll("[data-lang-option]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langOption === lang);
  });
}

function setupLanguageToggle() {
  const initial = localStorage.getItem(storageKey) || "zh";
  translatePage(initial);

  document.querySelectorAll("[data-lang-option]").forEach((button) => {
    button.addEventListener("click", () => {
      const nextLang = button.dataset.langOption;
      localStorage.setItem(storageKey, nextLang);
      translatePage(nextLang);
    });
  });
}

function setupReveal() {
  const nodes = document.querySelectorAll(".reveal");
  if (!nodes.length) {
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.16,
      rootMargin: "0px 0px -8% 0px"
    }
  );

  nodes.forEach((node) => observer.observe(node));
}

document.addEventListener("DOMContentLoaded", () => {
  setupLanguageToggle();
  setupReveal();
});
